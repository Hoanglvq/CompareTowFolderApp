    private void checkTheSameFolders(int numFolder) {
        if (bbObject.getFolder1Path().equals(bbObject.getFolder2Path())) {
            JOptionPane.showMessageDialog(this,
                    "Vui lòng chọn 2 folder khác nhau !",
                    "Inane warning",
                    JOptionPane.WARNING_MESSAGE);
            if(numFolder == 1){
                txtPath1.setText("");
                bbObject.emptyListFilesInFolder(1);
                this.btnOF1ActionPerformed(null);
            }else{
                txtPath2.setText("");
                bbObject.emptyListFilesInFolder(2);
                this.btnOF2ActionPerformed(null);
            }
        }
    }

